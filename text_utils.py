def reverse_string(string):                                                            #function to reverse a string and return it.
    return string[::-1]

def capitalize_first_word(string):                                                     #function to capitalize the first word in a string and return it.
    return string.capitalize()

def capitalize_all_words(string):                                                      #function to capitalize every word in a string and return it.
    return string.title()

def lowercase(string):                                                                 #function to make every letter of a string lower-case and return it.
    return string.lower()

def uppercase(string):                                                                 #function to captialize every letter of a string and return it.
    return string.upper()
